package br.com.dio.model;

import org.w3c.dom.ls.LSInput;

import java.util.Scanner;

public class Gato {
    private String nome;
    private String cor;
    private int idade;


    public Gato() {
        this.nome = nome;
        this.cor = cor;
        this.idade = idade;

    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }


}
